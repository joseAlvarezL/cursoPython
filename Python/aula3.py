"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas

"""
print(1234, end="\r\n\n\n")
# Aspas simples
print('Luiz Otávio',)
print('Luiz "Otávio"')
print(1, 'Luiz "Otávio"', end="\r\n\n\n")

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz f'Otávio'", end="\r\n\n\n")

# Escape
print("Luiz \"Otávio\"", end="\r\n\n\n")

# r
print(r"Luiz \"Otávio\"", end="\r\n\n\n")